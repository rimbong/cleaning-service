-- =====================================================================
-- V23 약품 품목에 pH 구분 추가
--
--   청소 약품은 "오염의 성질과 반대되는 pH를 쓴다"가 선택의 기본이라, 품목마다 성질을
--   달아두면 재고 목록에서 바로 구분이 된다.
--
--   더 중요한 이유는 안전이다. 염소계 표백제(락스)와 산성 세제를 섞으면 염소가스가 나온다.
--   보유 약품에 그 조합이 같이 있으면 화면에서 경고를 띄우기 위해 분류가 필요하다.
--
--   ※ 엔티티(biz/supply/domain/SupplyItem)와 일치해야 한다(ddl-auto=validate).
-- =====================================================================

ALTER TABLE supply_item ADD COLUMN ph_type VARCHAR(20);

COMMENT ON COLUMN supply_item.ph_type IS
    'ACID(산성) / NEUTRAL(중성) / ALKALI(알칼리성) / OXIDIZER(표백·산화계) / ENZYME(효소계) / ETC(기타). NULL 은 미분류';
