-- =====================================================================
-- V24 단가 정책 메모 복구 (V22 한글 깨짐)
--
--   [증상] V22 가 INSERT 한 메모의 한글이 전부 물음표로 저장됐다.
--          '2026년 최저임금...' -> '2026? ????...' (한글 1자 -> ? 1개)
--
--   [원인] DB·JDBC 문제가 아니다. 서버 인코딩은 UTF8 이고, 화면에서 저장한 한글은
--          정상이며, 같은 Flyway 경로로 넣은 한글도 다른 JVM 에서는 멀쩡했다.
--          마이그레이션을 적용한 JVM 의 기본 문자셋에 따라 결과가 달라진다
--          (IDE 실행 / mvn spring-boot:run / java -jar 이 서로 다를 수 있다).
--
--   [대책] 마이그레이션이 넣는 <b>값(데이터)</b>에는 한글을 직접 쓰지 않는다.
--          아래처럼 PostgreSQL 유니코드 이스케이프 U&'...' 를 쓰면 파일에는 ASCII 만
--          남고 한글은 DB 서버가 직접 만들어내므로, 어떤 JVM 으로 돌려도 같은 결과가 나온다.
--          (주석의 한글은 저장되는 값이 아니라 무해하다)
--
--   아래 값의 원문: 2026년 최저임금(시급 10,320원) 기준 초기 단가
-- =====================================================================

UPDATE pricing_policy
   SET memo = U&'2026\B144 \CD5C\C800\C784\AE08(\C2DC\AE09 10,320\C6D0) \AE30\C900 \CD08\AE30 \B2E8\AC00',
       updated_at = NOW()
 WHERE memo IS NULL
    OR memo = ''
    OR memo LIKE '%?%';
